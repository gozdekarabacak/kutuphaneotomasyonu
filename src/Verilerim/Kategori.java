package Verilerim;

public class Kategori {

    int tur_id;
    String tur_adi, stant_no, stant_adres, eklenme_tarihi;

    public int getTur_id() {
        return tur_id;
    }

    public void setTur_id(int tur_id) {
        this.tur_id = tur_id;
    }

    public String getTur_adi() {
        return tur_adi;
    }

    public void setTur_adi(String tur_adi) {
        this.tur_adi = tur_adi;
    }

    public String getStant_no() {
        return stant_no;
    }

    public void setStant_no(String stant_no) {
        this.stant_no = stant_no;
    }

    public String getStant_adres() {
        return stant_adres;
    }

    public void setStant_adres(String stant_adres) {
        this.stant_adres = stant_adres;
    }

    public String getEklenme_tarihi() {
        return eklenme_tarihi;
    }

    public void setEklenme_tarihi(String eklenme_tarihi) {
        this.eklenme_tarihi = eklenme_tarihi;
    }

}
